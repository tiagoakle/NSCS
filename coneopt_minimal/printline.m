function printline(sz,type)

if sz == 2 || sz == 12 || sz == 22
    if type == 1
        fprintf(['------------------------------------',...
            '-------------------------------------\n']);
    else
        fprintf(['====================================',...
            '===================================== \n']);
    end
else
    if type == 1
        fprintf(['--------------------------------------------',...
            '---------------------------------------------\n']);
    else
        fprintf(['=============================================',...
            '============================================\n']);
    end
end